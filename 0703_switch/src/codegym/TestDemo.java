package codegym;

public class TestDemo {
	public static void main(String[] args) {
		char c = 'x';

		switch (c) {
			case 'z':
				System.out.println("z");
				break;
			case 'y':
				System.out.println("y");
				break;
			case 'x':
				System.out.println("x");
				break;
			default:
				System.out.println("default");
			}
	}
}
